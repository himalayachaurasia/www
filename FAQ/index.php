				<div class="row">
					<h1>FREQUENTLY ASKED QUESTIONS</h1>
				</div>
				<div class="row faq">
					<h2>Q1. What is market value?</h2>
					<p>
						A. Market value in relation to any property which is the subject matter of an instrument means the price which such property would have fetched if sold in the open market on the date of execution of such instrument or the consideration mentioned in the instrument, whichever is higher. The price which such property would have fetched if sold in the open market is determined on the basis of the ready Reckoned issued each year.
					</p>
				</div>
				<div class="row faq">
					<h2>Q2. What is procedure for registration of an instrument?</h2>
					<p>
						A. Once adequate stamp duty is affixed on an instrument and it is dated, signed by the parties and attested (where required) by witnesses, it can be lodged for registration after payment of the registration fee. All parties signing the instrument are required to attend the office of the concerned Sub Register of Assurances either by themselves or through their constituted  attorney under a power of attorney to admit execution of the instrument.
					</p>
					<p>
						A passport size photograph, original power of attorney, personal identification such as passport or income tax PAN Card, adequate Xerox Copies of the original instrument are some of the essentials required for registration. After lodging an instrument. it is registered and seal of the Sub Register is affixed on the instrument, thereafter the original instrument is returned back to the parties.
					</p>
				</div>
				<div class="row faq">
					<h2>Q.3 Can a person grant a power of attorney of signing and registering instrument?</h2>
					<p>
						A. Yes. Persons residing abroad or those who travel frequently are advised to grant power of attorney to facilitate better management of their flats. The system promotes transparency in transactions, clearly sets out mutual obligations of parties and increases clarity in response action, when required.
					</p>
				</div>
				<div class="row faq">
					<h2>Q.4 Which documents should a flat purchaser ask for at the time of booking a flat? </h2>
					<p>
						A. A flat purchaser can ask for the certificate of title issued by an advocate, which gives the history of the ownership of the land under construction. Other documents such as applicable municipal approvals and sanctioned plans can also be inspected.
					</p>
				</div>
				<div class="row faq">
					<h2>Q5. How ill a flat purchaser know that his next installment is due?</h2>
					<p>A. The sales team communicates with flat purchasers through call letters, telephone and e-mail informing them about outstanding payment towards cost of the flat, society deposits and other charges. Receipts are issued from time to time for payments made by flat purchasers. </p>
				</div>
				<div class="row faq">
					<h2>Q6. How will a flat purchaser know if a building, which is under construction, in which he has booked a flat, is completed?</h2>
					<p>
						A. After completion of construction of the building, the municipal  corporation issues the occupation certificate. The sales team sends out call letters informing flat purchasers about the same.
					</p>
				</div>
